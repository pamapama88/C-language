//程序调用自身的编程技称为为递归（recurision）.
//它通常把一个大型的复杂的问题层层转化为一个与原问题相似的规模较小的问题来求解，递归策略只需要少量的程序就可以描述出解题过程所需要的多次重复计算，大大减少代码量。
//主要思考方式在于： 把大事化小
// 
//递归的两个必要条件
//1、存在限制条件，当满足这个限制条件的时候，递归不再继续
//2、每次递归调用之后越来越接近这个限制条件。

//下面看一个史上最简单的递归

